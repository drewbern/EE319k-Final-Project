void deathMenu(uint16_t score_In) {
	uint8_t data[3];
	data[0] = 0x04;
	data[1] = 0;
	data[2] = 0;
		
	for(uint8_t n = 0; n < 3; n++)
		UART_OutChar(data[n]);
	
	data[0] = 0x02;
	data[1] = score_In&0x00FF;
	uint16_t tempScore = score_In;
	tempScore = tempScore >> 8;
	data[2] = tempScore&0x00FF;
	
	for(uint8_t n = 0; n < 3; n++)
		UART_OutChar(data[n]);
	
	while((GPIO_PORTE_DATA_R & 0x20) == 0) {						//No button pressed
		uint32_t score = score_In;
		
		ST7735_DrawString(8, 5, "SCORE", 0xFFFF);
		
		char outString[5];
		
		char out = score/10000 + 0x30;
		outString[0] = out;
		score %= 10000;
		
		out = score/1000 + 0x30;
		outString[1] = out;
		score %= 1000;
		
		out = score/100 + 0x30;
		outString[2] = out;
		score %= 100;
		
		out = score/10 + 0x30;
		outString[3] = out;
		score %= 10;
		
		out = score + 0x30;
		outString[4] = out;
		
		ST7735_DrawString(8, 6, outString, 0xFFFF);
	}		
}
